    {
        //
        $dados = new Produto();
        $dados->nome = $request->input('nomeProduto');
        $dados->estoque = $request->input('quantidadeEstoque');
        $dados->preco = $request->input('preco');
        $dados->categoria_id = $request->input('categoria');
        $dados->save();
        return redirect('/produtos');
    }

